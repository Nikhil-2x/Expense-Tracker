    private void displayCat(){
        try{
           category.removeAllItems();
           ResultSet rs=db.Connections.st.executeQuery("SELECT * FROM category_info");
           while(rs.next()){
               category.addItem(rs.getString("category_name"));
           }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    private void getentries(){
        try{
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            java.time.LocalDate cd = java.time.LocalDate.now();
            java.time.LocalDate bd = cd.minusDays(20);
            ResultSet rs=db.Connections.st.executeQuery("SELECT * FROM spendings WHERE expense_date<='"+cd+"' and expense_date>='"+bd+"'");
            
            int total=0;
            while(rs.next()){
                total+=rs.getInt("amount");
                Object o[] = {rs.getInt("expense_id"),rs.getString("category_name"),rs.getDate("expense_date"),rs.getInt("amount")};
                dtm.addRow(o);
            }
            TotalAmount.setText(total+"");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    

