    private void addtocolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addtocolActionPerformed
        try{
            java.util.Date dt = DateChooser.getDate();
            String s1 = amt.getText();
            String c = (String)category.getSelectedItem();
            if(dt!=null && !s1.equals("") && !c.equals("")){
                int amount = Integer.parseInt(s1);
                java.sql.Date date= new java.sql.Date(dt.getTime());
                db.Connections.st.executeUpdate("INSERT INTO spendings (category_name,expense_date,amount) VALUES ('"+c+"','"+date+"','"+amount+"')");
                JOptionPane.showMessageDialog(null, "Expense Updated Successfully!");
                getentries();
                
            } else{
                JOptionPane.showMessageDialog(null, "One of the data is missing!");
            }
            
            ResultSet rs1 = db.Connections.st.executeQuery("SELECT budget_amount FROM budgets WHERE category_name='"+c+"'");
            int budgetAmount = 0;
            while(rs1.next()){
                budgetAmount = rs1.getInt("budget_amount");
            }
            Calendar calendar = Calendar.getInstance();
            calendar.setTime(dt);
            int month = calendar.get(Calendar.MONTH) + 1; // Adding 1 to get 1-based month
            int year = calendar.get(Calendar.YEAR);
            ResultSet rs2 = db.Connections.st.executeQuery("SELECT SUM(amount) AS total_expense FROM spendings WHERE category_name='"+c+"' AND MONTH(expense_date) ='"+month+"' AND YEAR(expense_date) ='"+year+"'");
            int totalExpense = 0;
            while(rs2.next()){
                totalExpense = rs2.getInt("total_expense");
            }
            if(totalExpense > budgetAmount)
                JOptionPane.showMessageDialog(null, "Warning: Budget for " + c + " has been exceeded for this month!");
            else if (totalExpense > budgetAmount - 500)
                JOptionPane.showMessageDialog(null, "Warning: You have less than Rs.500 to spend for " + c + " in this month!");
            else if (totalExpense > budgetAmount - 1000)
                JOptionPane.showMessageDialog(null, "Warning: You have less than Rs.1000 to spend for " + c + " in this month!");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_addtocolActionPerformed

